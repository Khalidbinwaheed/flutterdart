//Q2.Explain assignment operators in Dart. Write a program to demonstrate the use of =, +=, -=, *=, and /=.
void main() {
  double a = 10;
  double b = 20.0;
  a += b; // a = a + b
  print(a); // 30
  a -= b; // a = a - b
  print(a); // 10
  a *= b; // a = a * b
  print(a); // 200
  a /= b; // a = a / b
  print(a); // 10.0
}
