//Section 2: Operators in Dart
//Mandatory Questions
//Q1. What are relational operators in Dart? Write a program to demonstrate their usage (<, <=, >, >=, ==, !=).
void main() {
  int a = 10;
  int b = 20;
  print(a < b); // true
  print(a <= b); // true
  print(a > b); // false
  print(a >= b); // false
  print(a == b); // false
  print(a != b); // true
}
