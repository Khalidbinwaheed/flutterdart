//3. Write a program to declare and print a bool variable in Dart.
//bool is a data type in Dart that represents a boolean value. It can have two values: true or false.
//Example:
void main()
{
  bool isTrue = true; //Declaring a boolean variable isTrue and assigning it a value of true
  bool isFalse = false; //Declaring a boolean variable isFalse and assigning it a value of false

  print('isTrue: $isTrue'); //Printing the value of the boolean variable isTrue
  print('isFalse: $isFalse'); //Printing the value of the boolean variable isTrue
}